#insertion sort

def insertionsort(keywords_list):
  for i in range(0,len(keywords_list)):
    j = i
    while(j > 0 and keywords_list[j-1][0] > keywords_list[j][0]):
      temp = keywords_list[j]
      keywords_list[j] = keywords_list[j-1]
      keywords_list[j-1] = temp
      j = j-1

insertionsort(keywords_list)
print(keywords_list)

#quick sort

def partition(keywords_list, low, high):
  pivot = keywords_list[high]
  index = low

  for i in range(low, high):
    if keywords_list[i] <= pivot:
      temp = keywords_list[index]
      keywords_list[index] = keywords_list[i]
      keywords_list[i] = temp
      index = index + 1

  temp = keywords_list[high]
  keywords_list[high] = keywords_list[index]
  keywords_list[index] = temp

  return index

def qsort(keywords_list, low, high):
  if low < high:
    pivot = partition(keywords_list, low, high)

    qsort(keywords_list, low, pivot - 1);
    qsort(keywords_list, pivot + 1, high);

def quicksort(keywords_list):
    qsort(keywords_list, 0, len(keywords_list) - 1)

