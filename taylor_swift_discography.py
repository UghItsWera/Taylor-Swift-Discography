def bubblesort(keywords_list):
  for i in range(0, len(keywords_list)):
    for j in range(0,len(keywords_list) - 1):
      if keywords_list[j] [0] > keywords_list[j+1][0]:
        temp = keywords_list[j] + keywords_list [j+1]
        keywords_list[j+1] = temp

